public class Person {
    //Atributos
    private String name;
    private String lastName;

    //Metodo
    public void setName (String name) {
        this.name = name;
    }
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
    public String getName(){
        return name;
    }
    public String getLastName() {
        return lastName;
    }
}

